# Conversion de tipos de datos
entero = 13245
flotante = 12.25
cadena = "12435"

entero_str = str(entero) # Conversion de entero a cadena de texto
flotante_int = int(flotante) # Conversion de flotante a entero
cadena_entero = int(cadena) # Conversion de cadena a entero

print("""
Conversiones de tipos de datos:
    Datos iniciales:
    entero = 13245
    flotante = 12.25
    cadena = "12435""")

# Impresion de tipos de datos
print("entero_str: ",type(entero_str))
print("flotante_int: ",type(flotante_int))
print("cadena_entero: ",type(cadena_entero))